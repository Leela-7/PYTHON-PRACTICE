x="krishna"
print("The string is:", x)
print("The length of the string is:", len(x))  # Print the length of the string
print("The string in uppercase is:", x.upper())  # Convert to uppercase
print("The string in lowercase is:", x.lower())  # Convert to lowercase
print("The string with first letter capitalized is:", x.capitalize())  # Capitalize the first letter
print("The string with all words capitalized is:", x.title())  # Capitalize the first letter of each word
print("The string with all letters in title case is:", x.swapcase())  # Swap case of all letters
print("The string with all letters in reverse case is:", x[::-1])  # Reverse the string