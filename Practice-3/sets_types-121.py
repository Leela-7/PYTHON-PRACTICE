A={1,2,3,4,5}
B={2,3,4,5,6}
print(A.union(B))  # Union of A and B,combines all unique elements from both sets |
print(A.intersection(B))  # Intersection of A and B,common elements in both sets &
print(A.difference(B))  # Elements in A but not in B  -
print(A.symmetric_difference(B))  # Elements in either A or B but not both  ^